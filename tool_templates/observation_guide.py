#!/usr/bin/env python3
"""
observation_guide.py
生成用户观察引导问卷，用于 L6 级语料收集（无公开资料的目标）
"""

from typing import Dict, List, Optional


# 通用观察维度（适用于所有目标类型）
GENERIC_OBSERVATION_TEMPLATE = """
## 📋 通用观察维度

请基于你对目标的了解，描述以下方面：

### 1. 基本印象
- 第一次见面/接触时，对方给你什么印象？
- 现在你对ta的印象发生了什么变化？

### 2. 思维模式
- ta面对问题时的第一反应是什么？（如：沉默、立刻回应、回避、激动）
- 遇到困难或压力时，ta通常如何应对？
- ta做决定时有什么特点？（犹豫/果断/依赖他人/凭直觉）

### 3. 表达风格
- ta说话有什么口头禅或习惯用语？
- ta的语气偏什么类型？（权威型/亲和型/幽默型/冷淡型/激动型）
- ta是喜欢长篇论述还是简短表达？

### 4. 行为模式
- ta在团队中通常扮演什么角色？（领导/执行者/协调者/旁观者）
- ta如何处理与他人的分歧或冲突？
- ta的时间观念如何？（准时/迟到/匆忙/拖延）

### 5. 情绪特征
- 什么类型的事情会让ta开心/生气/焦虑/沮丧？
- ta如何表达自己的情绪？（外露/内敛/掩饰）
- ta的情绪波动大吗？

### 6. 价值观倾向
- ta最看重什么？（成就/关系/安全/自由/认可）
- ta对什么事情会表现出强烈的态度或立场？
"""

# 目标类型特定的观察维度
TYPE_SPECIFIC_OBSERVATIONS = {
    "职场人物": """
### 7. 职场特征（额外补充）
- ta的工作风格是什么？（高强度/按部就班/灵活/保守）
- ta如何与上司/下属/同事相处？
- ta在会议上通常的表现？（发言积极/被动回应/主导讨论/沉默倾听）
- ta如何处理跨部门协作或冲突？
- ta对工作和生活的平衡态度如何？
""",

    "私密关系": """
### 7. 关系动态（额外补充）
- 你们的关系中，权力分配如何？（平等/一方主导）
- ta在亲密关系中通常扮演什么角色？（照顾者/被照顾者/独立型/依赖型）
- ta处理冲突的方式是什么？（冷战/争吵/回避/沟通/冷处理）
- ta表达爱意的方式？（言语/行动/礼物/陪伴/默默付出）
- ta的依恋风格是什么？（安全型/焦虑型/回避型/混乱型）
""",

    "自己": """
### 7. 自我成长（额外补充）
- 你认为自己的核心优势是什么？
- 你觉得自己的盲点或需要改进的地方是什么？
- 过去一年你最大的变化/成长是什么？
- 你希望三年后的自己是什么样的？
- 你做决策时最容易被什么因素影响？（情感/理性/他人意见/直觉/数据）
""",

    "虚构角色": """
### 7. 角色设定（额外补充）
- ta在故事中的核心动机是什么？
- ta的致命弱点/缺陷是什么？
- ta与其他主要角色的关系如何？
- ta的世界观/价值观是什么？
- ta的成长弧线是什么？（从...到...）
""",

    "风格流派": """
### 7. 风格特征（额外补充）
- 这个流派的核心方法论是什么？
- 与其他流派相比，这个风格最大的区别是什么？
- 什么场景最适合使用这种风格？
- 这种风格的局限性或缺点是什么？
"""
}


def generate_observation_guide(
    target_name: str,
    target_type: str,
    use_case: Optional[str] = None
) -> str:
    """
    生成针对特定目标的观察引导问卷

    Args:
        target_name: 目标名称
        target_type: 目标类型（职场人物、私密关系、自己、虚构角色、风格流派）
        use_case: 使用场景（可选）

    Returns:
        格式化的观察引导问卷
    """
    guide = f"""# 📝 观察引导问卷：{target_name}

> 本问卷用于帮助你系统性地整理对「{target_name}」的观察和记忆。
> 请尽量基于具体的例子和经历来回答，而非抽象概括。
> 回答没有对错之分，越具体越好。

---

{GENERIC_OBSERVATION_TEMPLATE}
"""

    # 添加类型特定的观察维度
    if target_type in TYPE_SPECIFIC_OBSERVATIONS:
        guide += TYPE_SPECIFIC_OBSERVATIONS[target_type]

    # 添加使用场景相关的提示
    if use_case:
        guide += f"""
---

### 8. 使用场景关联（{use_case}）

请特别描述与「{use_case}」相关的观察：
- 在这个场景下，ta通常会怎么做？
- 你最希望从这个skill中获得什么帮助？
- 有什么具体的例子可以说明你想要的效果？
"""

    guide += """
---

## 💡 填写提示

1. **尽量具体**：用具体事件/对话代替抽象描述
2. **多个例子**：同一个特征最好用2-3个不同例子支撑
3. **包含反例**：有时对方"不"做什么也能说明特征
4. **记录原话**：如果记得ta说过的原话，请一并记录

回答完这些问题后，请粘贴到 Meta-Distiller 的 L6 语料收集中。
"""

    return guide


def generate_mini_guide(target_name: str, target_type: str) -> str:
    """
    生成简化版观察引导（快速模式）
    """
    type_to_questions = {
        "职场人物": [
            "ta在工作中最看重什么？",
            "ta如何与不同意见的人相处？",
            "ta的领导风格是怎样的？"
        ],
        "私密关系": [
            "ta在关系中通常扮演什么角色？",
            "ta如何处理冲突？",
            "ta表达情感的方式是什么？"
        ],
        "自己": [
            "你的核心优势和劣势是什么？",
            "你做决定时最看重什么？",
            "你希望改进的方面是什么？"
        ],
        "虚构角色": [
            "这个角色最核心的特质是什么？",
            "ta的动机和目标是什么？",
            "ta与其他角色的关系如何？"
        ],
        "风格流派": [
            "这种风格的核心特征是什么？",
            "什么时候适合用这种风格？",
            "这种风格的典型例子有哪些？"
        ]
    }

    questions = type_to_questions.get(target_type, type_to_questions["职场人物"])

    return f"""# 快速观察引导：{target_name}

请用2-3句话描述：

1. {questions[0]}
2. {questions[1]}
3. {questions[2]}

如有具体例子或原话，请一并附上。
"""


if __name__ == "__main__":
    # 测试
    print(generate_observation_guide("张三", "职场人物", "写周报时模仿他的风格"))
    print("---MINI---")
    print(generate_mini_guide("张三", "职场人物"))
